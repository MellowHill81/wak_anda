#While loop

#incrementing values
number = 5
while number <= 10 :
    print(number)
    number += 1

#decrementing values
num = 30
for num in range(30, 24, -1): #
    print(num)

# For loop
for i in range(105,110,2):
    print("number is",i)

devices = ["laptop", "mouse", "computer"]
for y in devices:
    print(y)