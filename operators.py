#Arithimetic operators

x = 44
y = 32
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #modulas - it returns the reminder after division

# Comparison Operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) # Equal to
print(x != y) # Not equal to

# Assignment Operators
a = 10
print(a)

a += 2
print(a)

#Logical Operators - we have and, or, not
print(34 < 50 and 56 > 200)
print(34 < 50 or 56 > 200)
print(not(34 < 50 or 56 > 200))